package StreakTheSpire.UI;

public class IntMargins {
    public int left = 0;
    public int right = 0;
    public int top = 0;
    public int bottom = 0;

    public IntMargins() {}

    public IntMargins(int left, int right, int top, int bottom) {
        this.left = left;
        this.right = right;
        this.top = top;
        this.bottom = bottom;
    }
}
