package StreakTheSpire.UI;

public class Padding {
    public int up;
    public int down;
    public int left;
    public int right;

    public Padding() {
    }

    public Padding(int up, int down, int left, int right) {
        this.up = up;
        this.down = down;
        this.left = left;
        this.right = right;
    }
}
